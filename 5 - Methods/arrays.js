let produtos = ['Carrinho', 'Boneca', 'Bola']

//.push() adiciona ao final da lista
produtos.push('Navio', 'Corda')
console.log(produtos)

//.unshift() adiciona no inicio da lista
produtos.unshift('Videogame')
console.log(produtos)

//.pop() remove o ultimo elemento
produtos.pop()
console.log(produtos)

//.shift() remove o primeiro elemento
produtos.shift()
console.log(produtos)


